
//FOR - laço de repetição definido.
for (i = 0; i < 5; i++){
    console.log("Feito!");
}

//DO..WHILE - laço de repetição indefinido com execução antes.
do {

    console.log("Arrenbentou!");

}while (i < 5)



//WHILE - laço de repetição indefinido.
var cont = 0;
while (cont < 5){

    console.log("Pizza!");
    cont++
}